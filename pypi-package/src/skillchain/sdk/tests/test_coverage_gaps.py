class TestExceptionHierarchy:
    """Verify all exceptions inherit from SkillChainError."""

    def test_all_exceptions_inherit_base(self):
        from skillchain.sdk.exceptions import (
            SkillChainError, ValidationError, ChainError,
            IPFSError, IdentityError, PackingError,
        )
        for exc_class in (ValidationError, ChainError, IPFSError, IdentityError, PackingError):
            assert issubclass(exc_class, SkillChainError)

    def test_catch_base_catches_all(self):
        from skillchain.sdk.exceptions import SkillChainError, ChainError
        with pytest.raises(SkillChainError):
            raise ChainError("test")
