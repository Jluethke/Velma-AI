class TestSkillChainConfig:
    """Tests for SkillChainConfig load/save/keystore/trust_cache."""

    def test_load_defaults_to_sepolia(self):
        from skillchain.sdk.config import SkillChainConfig, NETWORK_PRESETS
        cfg = SkillChainConfig.load(network="sepolia")
        assert cfg.network == "sepolia"
        # rpc_url may be overridden by ~/.skillchain/config.json on disk;
        # verify the network was resolved correctly
        assert cfg.chain_id in ("84532", NETWORK_PRESETS["sepolia"]["chain_id"], "")

    def test_load_mainnet(self):
        from skillchain.sdk.config import SkillChainConfig
        cfg = SkillChainConfig.load(network="mainnet")
        assert cfg.network == "mainnet"

    def test_load_env_var_override(self):
        from skillchain.sdk.config import SkillChainConfig
        with patch.dict(os.environ, {"SKILLCHAIN_RPC_URL": "http://custom-rpc"}):
            cfg = SkillChainConfig.load(network="sepolia")
            assert cfg.rpc_url == "http://custom-rpc"

    def test_save_and_reload(self, tmp_path):
        from skillchain.sdk.config import SkillChainConfig
        cfg = SkillChainConfig()
        cfg.network = "sepolia"
        cfg.node_id = "test-node-123"
        cfg.agent_platform = "claude"
        cfg.domain_tags = ["reasoning", "code"]
        # Override paths to tmp
        cfg.config_dir = tmp_path
        cfg.config_file = tmp_path / "config.json"
        cfg.keystore_file = tmp_path / "keystore.json"
        cfg.trust_cache_file = tmp_path / "trust_cache.json"
        cfg.skills_dir = tmp_path / "skills"
        cfg.validations_dir = tmp_path / "validations"
        cfg.save()
        assert cfg.config_file.exists()
        data = json.loads(cfg.config_file.read_text(encoding="utf-8"))
        assert data["node_id"] == "test-node-123"
        assert data["domain_tags"] == ["reasoning", "code"]
        # Private key must NOT be in saved config
        assert "private_key" not in data

    def test_save_keystore_and_load(self, tmp_path):
        from skillchain.sdk.config import SkillChainConfig
        cfg = SkillChainConfig()
        cfg.config_dir = tmp_path
        cfg.keystore_file = tmp_path / "keystore.json"
        ks = {"node_id": "ks-node", "wallet_address": "0xabc"}
        cfg.save_keystore(ks)
        assert cfg.keystore_file.exists()
        loaded = cfg.load_keystore()
        assert loaded["node_id"] == "ks-node"

    def test_load_keystore_missing_raises(self, tmp_path):
        from skillchain.sdk.config import SkillChainConfig
        from skillchain.sdk.exceptions import IdentityError
        cfg = SkillChainConfig()
        cfg.keystore_file = tmp_path / "nonexistent.json"
        with pytest.raises(IdentityError):
            cfg.load_keystore()

    def test_trust_cache_roundtrip(self, tmp_path):
        from skillchain.sdk.config import SkillChainConfig
        cfg = SkillChainConfig()
        cfg.trust_cache_file = tmp_path / "trust_cache.json"
        cache = {"peer-1": 0.85, "peer-2": 0.42}
        cfg.save_trust_cache(cache)
        loaded = cfg.load_trust_cache()
        assert loaded["peer-1"] == 0.85
        assert loaded["peer-2"] == 0.42

    def test_trust_cache_missing_returns_empty(self, tmp_path):
        from skillchain.sdk.config import SkillChainConfig
        cfg = SkillChainConfig()
        cfg.trust_cache_file = tmp_path / "nope.json"
        assert cfg.load_trust_cache() == {}

    def test_to_dict_excludes_private_key(self):
        from skillchain.sdk.config import SkillChainConfig
        cfg = SkillChainConfig()
        cfg.private_key = "secret"
        d = cfg.to_dict()
        assert "private_key" not in d
        assert "network" in d
        assert "node_id" in d


